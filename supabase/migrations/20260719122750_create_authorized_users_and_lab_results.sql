create table public.authorized_users (
  email text primary key,
  display_name text not null default '',
  role text not null check (role in ('Admin', 'User')),
  active boolean not null default true,
  added_at timestamptz not null default now(),
  notes text not null default '',
  constraint authorized_users_email_normalized
    check (email = lower(btrim(email)) and position('@' in email) > 1)
);

create table public.lab_result_rows (
  id bigint generated by default as identity primary key,
  source_tab text not null check (
    source_tab in (
      'ELISA Results',
      'HI Results',
      'PCR Results',
      'Antibiotic Results',
      'Bacteriology Results'
    )
  ),
  source_row integer not null check (source_row >= 2),
  test_id text not null,
  test_date date,
  customer_name text not null default '',
  farm_name text not null default '',
  lab_name text not null default '',
  area text not null default '',
  field_force text not null default '',
  sample_type text not null default '',
  test_category text not null,
  detail text not null default '',
  result text not null default '',
  sample_count numeric not null default 0,
  status text not null default '',
  quality_flag text not null default '',
  added_at timestamptz not null default now(),
  added_by text not null default '',
  payload jsonb not null default '{}'::jsonb,
  unique (source_tab, source_row)
);

create index lab_result_rows_test_date_idx
  on public.lab_result_rows (test_date desc);

create index lab_result_rows_category_idx
  on public.lab_result_rows (test_category);

create index lab_result_rows_customer_idx
  on public.lab_result_rows (customer_name);

create index lab_result_rows_added_at_idx
  on public.lab_result_rows (added_at desc);

alter table public.authorized_users enable row level security;
alter table public.lab_result_rows enable row level security;

revoke all on table public.authorized_users from anon, authenticated;
revoke all on table public.lab_result_rows from anon, authenticated;
revoke all on sequence public.lab_result_rows_id_seq from anon, authenticated;

grant select on table public.authorized_users to authenticated;
grant select on table public.lab_result_rows to authenticated;

create policy "active users can read their own access record"
on public.authorized_users
for select
to authenticated
using (
  active
  and lower(email) =
    lower(coalesce(((select auth.jwt()) ->> 'email'), ''))
);

create policy "active approved users can read lab results"
on public.lab_result_rows
for select
to authenticated
using (
  exists (
    select 1
    from public.authorized_users as approved
    where approved.active
      and lower(approved.email) =
        lower(coalesce(((select auth.jwt()) ->> 'email'), ''))
  )
);
